package liftgenerator;

public class Lift {
		private String type;
		private int weight;
		private int reps;
		private int sets;
		
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public int getWeight() {
			return weight;
		}
		public void setWeight(int weight) {
			this.weight = weight;
		}
		public int getReps() {
			return reps;
		}
		public void setReps(int reps) {
			this.reps = reps;
		}
		public int getSets() {
			return sets;
		}
		public void setSets(int sets) {
			this.sets = sets;
		}
		
}
